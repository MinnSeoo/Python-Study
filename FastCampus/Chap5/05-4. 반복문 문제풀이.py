    print("출력 :", i, "\n")
    user_ans = input("입력 : ")
    print("")
    
    if user_ans != i:
        print("틀렸습니다.. 다시 도전하세요!\n")
        print("전체 문제 개수 : ", len(korean_list), "\n")
        # 리스트의 요소 개수를 구하고 싶으면 len 함수를 사용하여 구할 수 있다.  
        print("맞힌 개수 : ", cnt)
