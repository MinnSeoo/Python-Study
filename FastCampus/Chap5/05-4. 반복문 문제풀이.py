# 2) 학교에서 과제로 게임 메뉴 만들기 프로그램을 만들어 오라고 하였다.
#    [과제 내용] 
#    숫자1 입력 : "게임을 시작합니다" 출력
#    숫자2 입력 : "실시간 랭킹" 출력
#    숫자3 입력 : "게임을 종료합니다" 출력 후 프로그램 종료
#    (단, 3을 입력할 때까지 프로그램은 계속 실행된다.)
#    (1 ~ 3외 다른 숫자를 입력한 경우 "다시 입력해 주세요" 를 출력)

# [내가 푼 코드]
